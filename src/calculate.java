    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jTextField1.getText().equals("")) {
            JOptionPane.showMessageDialog(rootPane, "اختر التقدير:");
        } else if (jComboBox1.getSelectedItem().equals("اختر التقدير:")) {
            JOptionPane.showMessageDialog(rootPane, "اختر التقدير:");
        } else {
            if (jTextField1.getText().equals("مقبول")) {
                group_degree.add("55");
            } else if (jTextField1.getText().equals("جيد")) {
                group_degree.add("65");
            } else if (jTextField1.getText().equals("جيد جدا")) {
                group_degree.add("75");
            } else if (jTextField1.getText().equals("امتياز")) {
                group_degree.add("85");
            }

            jComboBox1.setSelectedIndex(0);
            jTextField1.setText("");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

