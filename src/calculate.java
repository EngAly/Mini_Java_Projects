    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int result = 0;
            int r = 0;
            int count = 0;
            for (int i = 0; i < group_degree.size(); i++) {
                result += Integer.parseInt((String) group_degree.get(i));
                count++;
            }
            r = (result * 100) / (count * 100);
            System.out.println(r);
            if (r >= 55 && r < 65) {
                jLabel2.setText("مقبول");
            } else if (r >= 65 && r < 75) {
                jLabel2.setText("جيد");
            } else if (r >= 75 && r < 85) {
                jLabel2.setText("جيد جدا");
            } else if (r >= 85) {
                jLabel2.setText("امتياز");
            }

            group_degree.clear();

        } catch (Exception e) {

        }
    }//GEN-LAST:event_jButton1ActionPerformed

